CREATE TABLE CUSTOMERS
(
	CUST_ID DECIMAL (6,0) PRIMARY KEY,
	FIRST_NAME CHAR(10) NOT NULL,
	LAST_NAME CHAR(10) NOT NULL,
	ADDRESS CHAR(25),
	STATE CHAR (2),
	CITY CHAR (25),
	EMAIL CHAR(25),
	VIP_STATUS CHAR(3)
);

CREATE TABLE EMPLOYEES
(
	EMP_ID DECIMAL (6,0) PRIMARY KEY,
	FIRST_NAME CHAR(10) NOT NULL,
	LAST_NAME CHAR(10) NOT NULL,
	HRS_WORKED_BIWEEKLY DECIMAL (4, 0),
	POSITION CHAR (25) NOT NULL
);
	
CREATE TABLE INVENTORY
(
	ITEM_ID CHAR (5) PRIMARY KEY,
	PART_NAME CHAR (25),
	PART_TYPE CHAR (10),
	IN_STOCK DECIMAL (4, 0),
	PRICE DECIMAL (6,2)
);

CREATE TABLE INVOICE
(
	ORDER_NUM DECIMAL (10, 0) PRIMARY KEY,
	DATE_ORDERED CHAR (8),
	CUST_ID DECIMAL (6,0),
	TOTAL_PRICE DECIMAL (6,2),
	EST_DELIVERY_DATE CHAR (8)
);

